local effectObject = {}

effectObject.onEffectGain = function(target, effect)
    target:addMod(xi.mod.TREASURE_HUNTER, 40)
end

effectObject.onEffectTick = function(target, effect)
end

effectObject.onEffectLose = function(target, effect)
